public class EP_0 {
    public static void main(String[] args) {
        int i=0;
        while(i<20)
        {
            System.out.println(i);
            ++i;
        }
        System.out.println("do while loop");
        int j=0;
        do{
            System.out.println(j);
            ++j;
        }while(j<10);
    }
}
